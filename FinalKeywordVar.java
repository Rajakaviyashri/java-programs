package navinutube;
//final keyword can be used with variable,method,class
public class FinalKeywordVar {
	public static void main(String[]args) {
//final with variable
//if we have to create a constant variable we can use final so we can't change once we assigned the value
    final int num=9;
    //num=8;
    System.out.println(num);
    }
}
